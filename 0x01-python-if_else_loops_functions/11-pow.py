# demonstrates how to compute a to the power of b and return the value
# demonstrates how to use a function to affect program output
# -----------------------------------------------------------


def pow(a, b):
    """Raises a number to a power

    Args:
        a: number argument
        b: power argument

    Returns:
        the result of exponentiation
    """
    return a**b
